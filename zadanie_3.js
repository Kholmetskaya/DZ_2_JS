// Пользователь вводит число, нужно создать массив из чисел от 0 до "минус" число пользователя

// Создаем массив с первым элементом равным 0
var array = [0];
// Получаем массив длиной равной введенному числу +1 (первый элемент уже есть=0)
array[+process.argv[2]] = 1;
// Циклом перебираем длину массива и присваиваем значение для каждого элемента
for(var i=1; i<array.length; i++){
    array[i]= -i;
}

console.log(array);
